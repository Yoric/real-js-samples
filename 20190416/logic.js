function addSuffix(url) {
  var urlSuffix = Enabler.getParameter('exit_suffix');
  var _url = '';
  _url = url;

  if (url) {
    var symbol = '?';
    if (url.indexOf('?') > -1) {
      symbol = '&';
    }
    _url = url + symbol + urlSuffix;
  }
  return _url;
}